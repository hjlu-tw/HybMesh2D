"""Authoritative C++ skeletons + parameter templates for unicones IBM DLLs.

The solver loads two kinds of user DLL via dlopen, each an ``extern "C"`` function
with a fixed signature (the implicit contract with the solver):

  * init-condition DLL  -> ``initQ_at_p(x,y,z, Q, dQdt, dQdx, dQdy, dQdz)``
        Q = (rho, rho*u, rho*v, energy, phi)   [2D, phi is the solid marker]
  * solid-motion DLL    -> ``get_6dof_vel(time, dt, p, center, force, torque,
                                          U, V, W, rho)``
        writes the solid velocity (U,V,W) and density at point p.

These signatures are kept here as the single source of truth so the GUI's
generated code always matches what the solver expects. If the solver changes a
prototype, update it here. Tier 1 = parameterised templates (no C++ needed);
Tier 2 = the user edits the generated source freely before compiling.

Pure Python, no Qt — importable by both the dialog and tests.
"""
from __future__ import annotations
from dataclasses import dataclass, field
from typing import Callable

INIT_COND = "init_cond"
SOLID_MOTION = "motion"

# extern "C" prototypes echoed verbatim into every generated source.
PROTO_INIT = (
    'extern "C" void initQ_at_p(double x, double y, double z,\n'
    "                           double* Q, double* dQdt,\n"
    "                           double* dQdx, double* dQdy, double* dQdz);"
)
PROTO_MOTION = (
    'extern "C" void get_6dof_vel(double time, double dt, double* p,\n'
    "                             double* center, double* force, double* torque,\n"
    "                             double* U, double* V, double* W, double* rho);"
)


def _n(v) -> str:
    """Format a parameter as a C++ double literal."""
    return repr(float(v))


@dataclass
class ParamSpec:
    key: str
    label: str
    default: float
    decimals: int = 4
    tip: str = ""


@dataclass
class TemplateSpec:
    key: str
    label: str
    dll_type: str
    description: str
    render: Callable[[dict], str]
    params: list[ParamSpec] = field(default_factory=list)


# --------------------------------------------------------------------------- #
# Init-condition renderers (function initQ_at_p)
# --------------------------------------------------------------------------- #
_INIT_HEAD = f"""//
//  unicones initial-condition DLL  (generated by HybMesh PreProcessor)
//
#include <cmath>
using namespace std;

{PROTO_INIT}

void
initQ_at_p(double x, double y, double z,
           double* Q, double* dQdt,
           double* dQdx, double* dQdy, double* dQdz) {{
  const int nvar = 5;
"""

_INIT_TAIL = """
  // Conservative vector Q = (rho, rho*u, rho*v, energy, phi)
  Q[0] = rho;
  Q[1] = rho * u;
  Q[2] = rho * v;
  Q[3] = p / (gamma - 1.0) + 0.5 * rho * (u * u + v * v + w * w);
  Q[4] = phi;

  for (int i = 0; i < nvar; ++i) { dQdt[i] = dQdx[i] = dQdy[i] = dQdz[i] = 0.0; }
}
"""


def _render_init_freestream(p: dict) -> str:
    return _INIT_HEAD + f"""  const double mach = {_n(p['mach'])};
  const double gamma = {_n(p['gamma'])};
  const double T = 1.0;

  double rho = {_n(p['rho'])};
  double u = {_n(p['u'])};
  double v = {_n(p['v'])};
  double w = {_n(p['w'])};
  double pres = rho * T / (gamma * mach * mach);
  double p = pres;          // pressure
  double phi = 0.0;         // no solid marking in this template
""" + _INIT_TAIL


def _render_init_rotating_disk(p: dict) -> str:
    return _INIT_HEAD + f"""  const double mach = {_n(p['mach'])};
  const double gamma = {_n(p['gamma'])};
  const double T = 1.0;
  const double x0 = {_n(p['cx'])};
  const double y0 = {_n(p['cy'])};
  const double radius = {_n(p['radius'])};
  const double ratio = {_n(p['ratio'])};   // edge velocity scale

  double rho = 1.0;
  double u = 1.0, v = 0.0, w = 0.0;
  double p = rho * T / (gamma * mach * mach);
  double phi = 0.0;

  double rx = x - x0, ry = y - y0;
  double rad = sqrt(rx * rx + ry * ry);
  if (rad * rad < radius * radius) {{        // inside the solid disk
    double ang = atan2(ry, rx);
    double Vel = rad / radius;
    phi = 1.0;
    u = -Vel * sin(ang) * ratio;
    v =  Vel * cos(ang) * ratio;
    w = 0.0;
  }}
""" + _INIT_TAIL


def _render_init_custom(p: dict) -> str:
    return _INIT_HEAD + """  const double mach = 0.2;
  const double gamma = 1.4;
  const double T = 1.0;

  double rho = 1.0;
  double u = 1.0, v = 0.0, w = 0.0;
  double p = rho * T / (gamma * mach * mach);
  double phi = 0.0;

  // TODO: set rho/u/v/w from (x, y, z); set phi = 1.0 inside the solid.
""" + _INIT_TAIL


# --------------------------------------------------------------------------- #
# Solid-motion renderers (function get_6dof_vel)
# --------------------------------------------------------------------------- #
_MOTION_HEAD = f"""//
//  unicones solid-phase motion DLL  (generated by HybMesh PreProcessor)
//
#include <cmath>
using namespace std;

{PROTO_MOTION}

void
get_6dof_vel(double time, double dt, double* p,
             double* center, double* force, double* torque,
             double* U, double* V, double* W, double* rho) {{
"""
_MOTION_TAIL = "}\n"


def _render_motion_stationary(p: dict) -> str:
    return _MOTION_HEAD + """  U[0] = 0.0;
  V[0] = 0.0;
  W[0] = 0.0;
  *rho = 1.0;
""" + _MOTION_TAIL


def _render_motion_rotation(p: dict) -> str:
    return _MOTION_HEAD + f"""  const double x0 = {_n(p['cx'])};
  const double y0 = {_n(p['cy'])};
  const double radius = {_n(p['radius'])};   // velocity = 1 at this radius
  const double ratio = {_n(p['ratio'])};

  double rx = p[0] - x0, ry = p[1] - y0;
  double rad = sqrt(rx * rx + ry * ry);
  double ang = atan2(ry, rx);
  double Vel = rad / radius;
  U[0] = -Vel * sin(ang) * ratio;
  V[0] =  Vel * cos(ang) * ratio;
  W[0] = 0.0;                  // 2D rotation
  *rho = 1.0;
""" + _MOTION_TAIL


def _render_motion_translation(p: dict) -> str:
    return _MOTION_HEAD + f"""  U[0] = {_n(p['ux'])};
  V[0] = {_n(p['uy'])};
  W[0] = {_n(p['uz'])};
  *rho = 1.0;
""" + _MOTION_TAIL


def _render_motion_custom(p: dict) -> str:
    return _MOTION_HEAD + """  // p[0..2] = point coordinates; center[], force[], torque[] are inputs.
  // TODO: set the solid velocity at this point.
  U[0] = 0.0;
  V[0] = 0.0;
  W[0] = 0.0;
  *rho = 1.0;
""" + _MOTION_TAIL


